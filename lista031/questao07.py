'''
Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3 números.
'''

print("")

a = int(input("Informe um número:"))
b = int(input("Informe outro número:"))
c = int(input("Informe mais um número:"))

media = (a + b + c) / 3

print("")

print("A média aritmética desses números é:", media)

print("")
